package com.wenwo.message.data;

public class PushData {
	private int count;
	private String questionId;
	private String answerId;
	private String userId;
	private String content;
	private int messageType;

	public PushData(String content, int count, String questionId, String answerId, String userId, int messageType) {
		this.content = content;
		this.count = count;
		this.questionId = questionId;
		this.answerId = answerId;
		this.userId = userId;
		this.messageType = messageType;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public String getQuestionId() {
		return questionId;
	}

	public void setQuestionId(String questionId) {
		this.questionId = questionId;
	}

	public String getAnswerId() {
		return answerId;
	}

	public void setAnswerId(String answerId) {
		this.answerId = answerId;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public int getMessageType() {
		return messageType;
	}

	public void setMessageType(int messageType) {
		this.messageType = messageType;
	}

	@Override
	public String toString() {
		return "PushData [count=" + count + ", questionId=" + questionId + ", answerId=" + answerId + ", userId="
				+ userId + ", content=" + content + "]";
	}
}
