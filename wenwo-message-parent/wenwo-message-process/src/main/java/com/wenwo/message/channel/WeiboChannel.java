package com.wenwo.message.channel;

import com.wenwo.platform.types.project.SubprojectType;
import com.wenwo.weibo4j.AccGroup;

/**
 * 消息发送通道
 * @author StanleyDing
 * @date 2013-9-20
 * @since 2.0
 * 
 * History：
 * Date,	By,		What
 * 2013-9-20,	StanleyDing, 	Create
 */
public class WeiboChannel {
	private SubprojectType subprojectType;
	private AccGroup accGroup;
	public WeiboChannel(SubprojectType subprojectType, AccGroup accGroup) {
		super();
		this.subprojectType = subprojectType;
		this.accGroup = accGroup;
	}
	public SubprojectType getSubprojectType() {
		return subprojectType;
	}
	public AccGroup getAccGroup() {
		return accGroup;
	}
	@Override
	public String toString() {
		return "WeiboChannel [subprojectType=" + subprojectType + ", accGroup="
				+ accGroup + "]";
	}
}
